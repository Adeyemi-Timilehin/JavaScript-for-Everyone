// Switch
var weather = "cloudy";
switch (weather) {
  case "rainy":
    console.log("It is raining. You need a rain coat.");
    break;
  case "cloudy":
    console.log("It might be cold you need a jacket.");
    break;
  case "sunny":
    console.log("Go out freely.");
    break;
  default:
    console.log("It is not raining. No need for rain coat.");
    break;
}
